    private void jButtonSimulerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonSimulerMouseClicked
        // TODO add your handling code here:
        float coefNiveau;
        switch (niveau) {
                case "expert":
                    coefNiveau = (float)3.399;
                    break;
                case "junior":
                    coefNiveau = (float)0.63;
                    break;
                default:
                    coefNiveau = 0;
                    break;
            }
        float vitesse = (float)(50.441+coefNiveau + 1.7155*this.poids*0.001+0.0143*this.puissance-0.0072*this.nbVirage);
        float temps = (float) this.distance/vitesse;
        
        String tmin = mf.transformerHeure(String.valueOf(temps*50/60));
        String tmax = mf.transformerHeure(String.valueOf(temps*70/60));
        
       this.jLabelTempsProbableMin.setText(tmin);
       this.jLabelTempsProbableMax.setText(tmax);
       
       
        
    }//GEN-LAST:event_jButtonSimulerMouseClicked

