    private void jCBRallyeItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jCBRallyeItemStateChanged
        // TODO add your handling code here:

        ArrayList<Date> list2 = new ArrayList<Date>();

        SimpleDateFormat df = new SimpleDateFormat("dd/MM/yyyy");
        //chercher rallye
        if(this.jCBRallye.getSelectedItem()!=null){
            try {
                for(Rallye rallye : mf.getListRallye()){
                    if(this.jCBRallye.getSelectedItem().equals(rallye.getNom())){
                        for(Edition e : rallye.getListEdition()){
                            list2.add(e.getDateDeb());
                        }
                    }
                }
            } catch (ParseException ex) {
                Logger.getLogger(Parieur_MonCompte.class.getName()).log(Level.SEVERE, null, ex);
            }
            //trouver l'edition plus nouvelle
            if(!list2.isEmpty()){
                Date max = list2.get(0);
                for(int i = 0; i<list2.size();i++){
                    int r = max.compareTo(list2.get(i));

                    if (r == -1){
                        max = list2.get(i);
                    }
                }

                // afficher la date de l'édition la plus nouvelle
                this.jLabelEditionNouvelle.setText(df.format(max));
                this.jCBEtape.removeAllItems();
                this.jTableCotation.removeAll();
                try {
                    // chercher la liste des étapes de l'édition la plus nouvelle du rallye selectioné
                    for(Rallye rallye : mf.getListRallye()){
                        if(this.jCBRallye.getSelectedItem().equals(rallye.getNom())){
                            for(Edition e : rallye.getListEdition()){
                                if(df.format(max).equals(df.format(e.getDateDeb()))){
                                    for(Etape eta : e.getListEtape()){
                                        this.jCBEtape.addItem(String.valueOf(eta.getCodeE()));
                                        this.jLabelDistance.setText(String.valueOf(eta.getDistance()));
                                        this.jLabelNbV.setText(String.valueOf(eta.getNbVirage()));
                                    }
                                }

                            }
                        }
                    }
                } catch (ParseException ex) {
                    Logger.getLogger(Parieur_MonCompte.class.getName()).log(Level.SEVERE, null, ex);
                }

            }
        }
    }//GEN-LAST:event_jCBRallyeItemStateChanged

