    private void jButtonValiderMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonValiderMouseClicked

        String dateNaissance = null;
        Boolean emptyFormulaire = true;
        Boolean valideMail =false;
        Boolean validePassword =false;
        errorName.setText("");
        errorPrenom.setText("");
        errorDate.setText("");
        errorMail.setText("");
        errorAdresse.setText("");
        errorIBAN.setText("");
        errorPasswordI.setText("");

        if (jTextNom.getText().trim().isEmpty()){
            errorName.setText("Veulliez entrer votre nom");
            emptyFormulaire = true;
        }else{
            emptyFormulaire = false;
        }
        if (jTextPrenom.getText().trim().isEmpty()){
            errorPrenom.setText("Veulliez entrer votre prenom");
            emptyFormulaire = true;
        }else{
            emptyFormulaire = false;
        }
        if (jDateChooser1.getDate() == null){
            errorDate.setText("Veulliez choisir votre date de naissance");
            emptyFormulaire = true;
        }else{
            emptyFormulaire = false;
        }
        if (jTextEmail.getText().equals("")){
            errorMail.setText("Veulliez entrez votre adresse mail");
            emptyFormulaire = true;
        }else{
            emptyFormulaire = false;
        }
        if (jTextAdresse.getText().trim().isEmpty()){
            errorAdresse.setText("Veulliez entrer votre adresse");
            emptyFormulaire = true;
        }else{
            emptyFormulaire = false;
        }
        if (jTextIBAN.getText().trim().isEmpty()){
            errorIBAN.setText("Veulliez entrer votre IBAN");
            emptyFormulaire = true;
        }else{
            emptyFormulaire = false;
        }
        //verifier email
        for(Parieur parieur: this.mf.getListParieur()){
            if(!parieur.getEmail().equals(this.jTextEmail.getText()) ){
                errorMail.setText("veuillez rentrez une adresse mail valide");
                emptyFormulaire = true;
            }
        }

        if (jPasswordI.getText().trim().isEmpty()){
            errorPasswordI.setText("Veulliez entrer un mot de passe");
            emptyFormulaire = true;
        }else{
            if (jPasswordI2.getText().trim().isEmpty()){
                errorPasswordI.setText("Veulliez confimer le mot de passe");
            }else{
                emptyFormulaire = false;
                //verifier mot de passe
                if (jPasswordI.getText().equals(jPasswordI2.getText())){
                    validePassword = true;
                }else{
                    errorPasswordI.setText("Les mots de passe ne sont pas identiques");
                }
            }
        }

        // verifier est ce que ce mail existe deja dans la base de donnees

        if(!emptyFormulaire && validePassword){

            try {

                Calendar now = Calendar.getInstance();
                Calendar dob = Calendar.getInstance();
                dob.setTime(jDateChooser1.getDate());
                int year1 = now.get(Calendar.YEAR);
                int year2 = dob.get(Calendar.YEAR);
                int age = year1 - year2;
                int month1 = now.get(Calendar.MONTH);
                int month2 = dob.get(Calendar.MONTH);
                if (month2 > month1) {
                    age--;
                } else if (month1 == month2) {
                    int day1 = now.get(Calendar.DAY_OF_MONTH);
                    int day2 = dob.get(Calendar.DAY_OF_MONTH);
                    if (day2 > day1) {
                        age--;
                    }
                }
                String ageS = String.valueOf(age);
                this.mf.enregistrerParieur(jTextEmail.getText(),jPasswordI.getText(), jTextNom.getText(), jTextPrenom.getText(),ageS, jTextAdresse.getText(), jTextIBAN.getText());

                this.dispose();
                Parieur_MonCompte Cparieur = new Parieur_MonCompte(this.jTextEmail.getText());
                this.setVisible(false);
                Cparieur.setVisible(true);
            } catch (ParseException ex) {
                Logger.getLogger(Parieur_Inscription.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_jButtonValiderMouseClicked

